#include"libft.h"

char	*ft_strdup(const char *s1)
{
	size_t	len;
	char	*s;

	len = ft_strlen(s1) + 1;
	s = ft_calloc(1, len);
	if (!s)
		return (NULL);
	ft_memcpy(s, s1, len);
	return (s);
}
